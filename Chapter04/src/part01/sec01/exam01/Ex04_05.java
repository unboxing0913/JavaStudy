package part01.sec01.exam01;

public class Ex04_05 {

	public static void main(String[] args) {
		int a=100,b=200;
		              //true , false  %s 쓴다                     
		System.out.printf("%d == %d는 %s 이다.\n",a,b,a==b);//관계연산자 이건 정수두개의 변수가 같냐라고 물어보는거고
		System.out.printf("%d != %d는 %s 이다.\n",a,b,a!=b);
		System.out.printf("%d > %d는 %s 이다.\n",a,b,a>b);
		System.out.printf("%d < %d는 %s 이다.\n",a,b,a<b);
		System.out.printf("%d >= %d는 %s 이다.\n",a,b,a>=b);
		System.out.printf("%d <= %d는 %s 이다.\n",a,b,a<=b);
		
		System.out.printf("%d = %d는 %s 이다.\n",a,b,a=b); //대입연산자 이거는 대입해서 a에 b를 대입한다.a,b는정수니깐 정수
		
		

	}

}
