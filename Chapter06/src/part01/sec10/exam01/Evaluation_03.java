package part01.sec10.exam01;

public class Evaluation_03 {

	public static void main(String[] args) {
	
		  int i,j;
		  int result;
		  for(i=2;i<=9;i++) {
			  for(j=1;j<=9;j++) {
				  result = i*j;
				  System.out.printf("%d X %d = %d\n",i,j,result);				
			  } System.out.println(); 
		  } 
	}

}
